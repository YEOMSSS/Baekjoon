# [Summary]

# W명으로 구성된 특수소대를 원타곤에 투입할 것이다.
# 원타곤은 N*2개구역이 원형으로 둘러싸여져 있는 구조다.

# 한 특수소대는 한 구역을 처리할 수 있고, 인접한 구역을 하나 더 처리할 수 있다.
# 구역의 합이 W보다 크지 않아야 처리가 가능하다.
# 한 구역을 두 특수소대가 처리하는 것은 불가능하다.

# 최소 몇개소대가 필요한지 구하시오.


def main() -> None:

    # [Ideas]

    # 일단 인접구역이랑 합해서 W를 넘어가면 그 구역에 1개소대를 편성한다.
    # 그리고 남은 구역들을 최대한 둘씩 묶어서 편성해야 한다.

    # 근데 이게 인접구역만 묶을 수 있다보니 그리디로는 안 된다. 이건 절대 아님.
    # 그렇다는 건 사실 모든 구역을 확인해봐야 한다는 건데, 브루트포스도 안 된다.

    # dp에 현재 칸까지 필요한 소대수를 저장하자.
    # 위 아래 우상 이런식으로 이동하면서 점화식을 굴려보자.
    # 그럼 3종류다. 혼자거나, 좌우거나, 상하거나. 상하는 아래쪽일때만 검사.

    # 점화식은, 혼자쓸땐 그냥 이전칸min에 1 더하면 되고
    # 좌우로 이어지면 전전칸혼자....... ??? 이거 이상하다. 좌우가 저장이 안된다.
    # 상하로 이어지면 전칸혼자
    # 그럼 dp 안에 자신이 혼자, 좌우, 상하일 때를 저장하면 된다.

    # 열 기준으로 생각해 보자...
    # 둘다 혼자거나, 상하거나, 위쪽이 이전과 연결되거나, 아래쪽이 이전과 연결되거나, 둘다 이전과 연결되거나.
    # 갈아엎을 필요가 있음. dp에 열과 열 사이 경계의 상태로 나누자.
    # 전부사용불가, 위쪽이 연결대기, 아래쪽이 연결대기, 위아래 다 연결대기 이렇게 4가지 경우가 있다.

    # 이번 열의 모든 경우에 대해 다음 행동을 확인한다.
    # 둘다 따로쓰기, 상하 합치기. 이건 이전열의 전부사용불가에 +2, +1을 하면 되고
    # 이전 위연결대기고 이번이 위연결대기를 안만들때 검사 후 +1
    # 이전 아래연결대기고 이번이 아래연결대기를 안만들때 검사 후 +1

    # dp는 대충 정리가 됐는데, 이제 문제는 원형이라는 거다.
    # 1열 2열을 볼 때 경우가 4가지가 있다.
    # 원통연결없음, 위좌우, 아래좌우, 위아래좌우. dp를 4번 돌리면 되네!!!!

    ##########
    def solve(i):
        # DP[state][n]
        # state (0~3):
        # 0: 현재 열 n에서 아무것도 연결 안된 상태
        # 1: 위쪽 가로 연결 진행 중
        # 2: 아래쪽 가로 연결 진행 중
        # 3: 위+아래 둘 다 가로 연결 진행 중

        # 초기값: 최댓값으로 설정
        DP = [[2 * N] * (N + 1) for i in range(4)]

        # 시작 상태 설정
        DP[i][-1] = 0

        for n in range(N):
            # 이전 상태(i) → 현재 상태 0으로 오는 경우
            # i의 각 비트:
            # bit 0 (1): 위쪽 연결 여부
            # bit 1 (2): 아래쪽 연결 여부
            for i in range(4):
                # 연결 안된 부분은 각각 1개씩 병력 필요
                DP[0][n] = min(
                    DP[0][n], DP[i][n - 1] + int(i & 1 == 0) + int(i & 2 == 0)
                )

            # 세로 묶기 (같은 열 위+아래)
            if enemy[0][n] + enemy[1][n] <= M:
                DP[0][n] = min(DP[0][n], DP[0][n - 1] + 1)

            # 위쪽 가로 묶기 (n, n+1)
            if enemy[0][n] + enemy[0][n + 1] <= M:
                # 이전 상태에 따라 비용 다름
                DP[1][n] = min(
                    DP[0][n - 1] + 2,  # 둘 다 따로 처리 후 연결
                    DP[2][n - 1] + 1,  # 아래는 이미 연결된 상태
                )

            # 아래쪽 가로 묶기
            if enemy[1][n] + enemy[1][n + 1] <= M:
                DP[2][n] = min(DP[0][n - 1] + 2, DP[1][n - 1] + 1)

            # 위+아래 동시에 가로 묶기
            if (
                enemy[0][n] + enemy[0][n + 1] <= M
                and enemy[1][n] + enemy[1][n + 1] <= M
            ):
                DP[3][n] = DP[0][n - 1] + 2

        return DP

    T = int(input())
    for _ in range(T):
        N, M = map(int, input().split())

        # enemy[0]: 위쪽
        # enemy[1]: 아래쪽
        # 마지막에 0 추가 (n+1 접근 대비)
        enemy = [[*map(int, input().split()), 0] for i in range(2)]

        # 기본 케이스 (원형 연결 없음)
        result = solve(0)[0][N - 1]

        # 위쪽 원형 연결 (0번 ↔ N-1번)
        if enemy[0][0] + enemy[0][N - 1] <= M:
            DP = solve(1)
            result = min(
                result,
                DP[2][N - 2] + 1,  # 마지막 아래 처리
                DP[0][N - 2] + 2,  # 둘 다 따로 처리
            )

        # 아래쪽 원형 연결
        if enemy[1][0] + enemy[1][N - 1] <= M:
            DP = solve(2)
            result = min(result, DP[1][N - 2] + 1, DP[0][N - 2] + 2)

        # 위+아래 둘 다 원형 연결
        if enemy[0][0] + enemy[0][N - 1] <= M and enemy[1][0] + enemy[1][N - 1] <= M:
            result = min(result, solve(3)[0][N - 2] + 2)

        print(result)

    ##########

    return


